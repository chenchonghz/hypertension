<p><h1>健康风险评估 - 你的健康快照！</h1></p>
<hr><br>
<p>&nbsp;&nbsp;&nbsp;&nbsp;健康风险评估（Health  Risk  Appraisal，HRA）是通过个人化的信息采集与分析来鉴别健康危险因素和判断健康风险的大小，并通过与干预措施的衔接来达到维护健康和预防疾病的效果，它是量化管理的重要手段。 </p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;健康风险评估利用生物信息学及循证医学的手段，采用IT支持技术，通过收集并跟踪反映个人健康状况的各种信息，计算出个人患某种疾病而死亡的可能性，并为个人提供个人健康信息清单、个人疾病危险性评价报告、个人健康危险因素提示及个性化膳食处方、运动处方。同时，健康还应用先进的互联网信息技术构建了功能强大的健康管理服务平台，提高医生工作效率，加强个人和医生之间的交流互动，进一步采取有效措施控制危险因素，为人群分类管理提供工具和服务以保证健康风险控制的效果和可持续性。 </p>
